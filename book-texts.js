// Функция для генерации контента книги по страницам
function generateBookContent(bookId, page) {
  const bookTexts = BOOK_TEXTS[bookId];
  if (!bookTexts) {
    return ['<p>Текст книги временно недоступен.</p>'];
  }

  const linesPerPage = 20; // Примерно 20 строк на страницу
  const startLine = (page - 1) * linesPerPage;
  const endLine = startLine + linesPerPage;
  const pageContent = bookTexts.slice(startLine, endLine);

  return pageContent.map(line => line ? `<p>${line}</p>` : '<br>');
}

// Экспортируем объект
window.BOOK_TEXTS = BOOK_TEXTS;
window.generateBookContent = generateBookContent;